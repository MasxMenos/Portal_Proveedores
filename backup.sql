CREATE FUNCTION public.nit_dv_co(nit_text text) RETURNS integer
    LANGUAGE plpgsql
    AS $$
DECLARE
  nit_clean text;
  arr int[] := ARRAY[3,7,13,17,19,23,29,37,41,43,47,53,59,67,71];
  s bigint := 0;
  d int;
  i int;
  L int;
BEGIN
  nit_clean := regexp_replace(nit_text, '\D', '', 'g');
  IF nit_clean = '' THEN
    RETURN NULL;
  END IF;
  L := length(nit_clean);
  -- recorre de derecha a izquierda (peso arr[1] para la última cifra)
  FOR i IN 1..L LOOP
    d := substr(nit_clean, L - i + 1, 1)::int;
    s := s + d * arr[i];
  END LOOP;
  RETURN (s % 11) % 10;
END;
$$;


